package hbm.java.app.hbm_java_app;


public class ConnectRequest {
  private String host;
  private String port;

  // Getters and setters
  public String getHost() {
      return host;
  }

  public void setHost(String host) {
      this.host = host;
  }

  public String getPort() {
      return port;
  }

  public void setPort(String port) {
      this.port = port;
  }
}